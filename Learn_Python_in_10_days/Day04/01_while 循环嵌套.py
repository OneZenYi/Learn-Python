'''
初始条件设置
while 条件(判断 计数器 是否达到 目标次数):
    条件满足是,做的事情
    条件满足是,做的事情
    处理条件(计数器+1)
    
    初始条件设置
    while 条件(判断 计数器 是否达到 目标次数):
        条件满足是,做的事情
        条件满足是,做的事情
        处理条件(计数器+1)
'''
# 循环嵌套  最好是从内到外做.
i = 0
while i < 3:
    print("我不开心!")
    j = 0
    while j < 3:
        print('你给我做饭')
        j += 1
    i += 1
print('完成任务')
